import java.io.Serializable;

/**
 * Class container for primitives
 */
public class MyClass implements Serializable {
	public String myString = "myString";
	public byte myByte = 1;
	public short myShort = 2;
	public int myInt = 3;
	public long myLong = 4L;
	public float myFloat = 5.5555555555555F;
	public double myDouble = 6.6666666666666D;
	public boolean myBoolean = true;
}

